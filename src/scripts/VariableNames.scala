//Variable names can have spaces
val `my variable name with spaces` = 20
println(`my variable name with spaces`)

//Variable names can have special characters with an underscore in the beginning
val isThisMySalary_? = 200
println(isThisMySalary_?)

//Back ticks can be used even to use reserved key words
val `import` = "Using a reserved keyword"
println(`import`)